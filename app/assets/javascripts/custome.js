
$(document).ready(function() {
  $('#new_join_event > .inputs > ol > li > #join_event_host_id').change(function() {

    var host_id = $(this).val();
    
    $.ajax({ 
     url : '/get_events', 
     type : 'POST', 
     dataType : 'json', 
     data : {host_id : host_id},
     success : function(e) {
        
        var str  = "";
              var str2 = "<option value =>select your event</option>" 
        $(e.events).each(function (index, value) { 
                
                str += "<option value ="+value[1]+">"+value[0]+"</option>"
    
     });
     str2 = str2 + str
     $("#new_join_event > .inputs > ol > li > #join_event_event_id").empty().append(str2);
     }
    		});
     
  });
});

$(document).ready(function() {
  $('#new_join_event > .inputs > ol > li > #join_event_event_id').change(function() {
    var event_id;
    event_id = $(this).val();
    $.ajax({
      url: '/get_guests',
      type: 'POST',
      dataType: 'json',
      data: {
        event_id: event_id
      },
      success: function(e) {
        var str, str2;
        str = '';
        str2 = '<option value =>Select your guest </option>';
        $(e.guests).each(function(index, value) {
          str += '<option value =' + value[1] + '>' + value[0] + '</option>';
        });
        str2 = str2 + str;
        $('#new_join_event > .inputs > ol > li > #join_event_user_id').empty().append(str2);
      }
    });
  });
});

